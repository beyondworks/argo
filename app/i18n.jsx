const LangCtx = createContext(null);

export function LanguageProvider({ children }) {
  const [lang, setLangState] = useState('ko');

  useEffect(() => {
    const saved = localStorage.getItem('argo-lang');
    if (saved === 'en' || saved === 'ko') setLangState(saved);
  }, []);

  const setLang = useCallback((next) => {
    setLangState(next);
    try { localStorage.setItem('argo-lang', next); } catch { /* 사파리 프라이빗 등 */ }
  }, []);

  // cmd+/ (윈도우는 ctrl+/) — 어디서든 언어 전환
  useEffect(() => {
    const h = (e) => {
      if ((e.metaKey || e.ctrlKey) && e.key === '/') {
        e.preventDefault();
        setLangState((l) => {
          const next = l === 'ko' ? 'en' : 'ko';
          try { localStorage.setItem('argo-lang', next); } catch { /* 무시 */ }
          return next;
        });
      }
    };
    window.addEventListener('keydown', h);
    return () => window.removeEventListener('keydown', h);
  }, []);

  const t = useCallback((key, vars) => {
    let s = DICT[key]?.[lang === 'ko' ? 0 : 1] ?? key;
    if (vars) for (const [k, v] of Object.entries(vars)) s = s.replaceAll(`{${k}}`, String(v));
    return s;
  }, [lang]);

  /** USD 내부값 → 표시 통화. ko는 원화 환산(고정 환율 — '약' 표기). */
  const fmtMoney = useCallback((usd, { approx = true } = {}) => {
    if (lang === 'ko') return `${approx ? '약 ' : ''}₩${Math.round(usd * KRW_RATE).toLocaleString('ko-KR')}`;
    return `$${usd.toFixed(2)}`;
  }, [lang]);

  return <LangCtx.Provider value={{ lang, t, fmtMoney, setLang }}>{children}</LangCtx.Provider>;
}

export function useLang() {
  const ctx = useContext(LangCtx);
  if (!ctx) throw new Error('useLang은 LanguageProvider 안에서만');
  return ctx;
}
